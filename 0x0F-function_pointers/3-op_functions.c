#include "3-calc.h"
/**
 *@a: int
 *@b: int
 *@op_add: add
 *Return: always 0
**/
int op_add(int a, int b)
{
return (a + b);
}
/**
 *@a: int
 *@b: int
 *@op_sub: sub
 *Return: always 0
 **/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *@a: int
 *@b: int
 *@op_mul: mul
 *Return: always 0
 **/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *@a: int
 *@b: int
 *@op_div: div
 *Return: always 0
 **/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *@a: int
 *@b: int
 *@op_mod: mod
 *Return: always 0
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
